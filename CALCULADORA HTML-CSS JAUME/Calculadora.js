class Calculadora {
    sumar(num1, num2){
        return (num1 + num2).toFixed(4);
    }

    restar(num1, num2){
        return (num1 - num2).toFixed(4);
    }

    dividir(num1, num2){
        return (num1 / num2).toFixed(4);
    }

    multiplicar(num1, num2){
        return (num1 * num2).toFixed(4);
    }
}